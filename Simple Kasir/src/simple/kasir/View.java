    private void editBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBarangActionPerformed
        id = jTextField4.getText();
        nama = jTextField5.getText();
        harga = jTextField6.getText();

        if ((nama == "") || (harga == "")) {
            JOptionPane.showMessageDialog(null, "Tidak boleh kosong !");
        } else if (!cekAngka(id)) {
            JOptionPane.showMessageDialog(null, "ID harus angka !");
        } else if ((cekAngka(nama))) {
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong dan harus huruf !");
        } else if ((!cekAngka(harga))) {
            JOptionPane.showMessageDialog(null, "Harga harus angka !");
        } else if ((nama != "") && harga != "" && id != "" && cekAngka(id) && (cekAngka(harga)) && !cekAngka(nama)) {
            Barang barang = new Barang(nama, harga, id);
            barang.updateBarang();
            showBarang();
            if (barang.ismasuk) {
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Tidak ada id cocok !");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan !");
        }
    }//GEN-LAST:event_editBarangActionPerformed

