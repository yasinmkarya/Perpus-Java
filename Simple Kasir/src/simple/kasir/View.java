    private void inputDataBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputDataBarangActionPerformed
        DatabaseMinikasir db = new DatabaseMinikasir();
        String id2 = (String) jTextField8.getText();
        String jumlah = jTextField1.getText();
        boolean status = true;
        if (cekAngka(id2) && cekAngka(jumlah) && id != "" && jumlah != "") {
            try {
                ResultSet rs = db.getData("select * from Barang");
                while (rs.next()) {
                    id = (String) rs.getString("id");
                    System.out.println(id);
                    if (id2.equals(id)) {
                        nama = rs.getString("nama");
                        harga = rs.getString("harga");
                        jTable3.setValueAt(id, j, 0);
                        jTable3.setValueAt(nama, j, 1);
                        jTable3.setValueAt(jumlah, j, 2);
                        jTable3.setValueAt(harga, j, 3);
                        jTextField8.setText("");
                        jTextField1.setText("");
                        System.out.println(id + nama + harga);
                        status = false;
                        j++;
                    }
                }
                if (status) {
                    JOptionPane.showMessageDialog(null, "Tidak ada ID yang cocok !");
                }
                rs.close();
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            if (id2 == "" || jumlah == "" || id2 == null || jumlah == null) {
                JOptionPane.showMessageDialog(null, "Masukan tidak boleh kosong !");
            }
            System.out.println("ID     :" + id2);
            System.out.println("Jumlah :" + jumlah);
            if (!cekAngka(id2) || !cekAngka(jumlah)) {
                JOptionPane.showMessageDialog(null, "Masukan salah, tidak boleh kosong dan harus angka !");
            }
        }
    }//GEN-LAST:event_inputDataBarangActionPerformed

